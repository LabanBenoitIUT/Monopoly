package fr.iutvalence.info.dut.m2107;

/**Cards of a Monopoly Game
 * @author canseva
 *
 */
public class Cartes
{

/**Attribute which represents the type of a card
 * 
 */
private String type;

/**
 * The text written on the card
 */
private String texte;

/**
 * Value of the card
 */
private int valeur;

/**
 * Using to get the type of a card
 * @return the type of a card
 */

public String getType()
{
	return type;
}


/**
 * Using to get the text of a card
 * @return the text of a card
 */
public String getTexte()
{
	return texte;
}


/**
 * Using to get the value of a card
 * @return the value of a card
 */
public int getValeur()
{
	return valeur;
}





}





